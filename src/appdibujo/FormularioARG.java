    private void jButtonImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImprimirActionPerformed
        //Si se selecciona la impresora Canon se lanza el mensaje de error
        String seleccion = (String) jComboBoxImpresora.getSelectedItem();
        String nombreArchivo = jTextFieldNombre.getText();

        //Simulaci贸n de riesgo de sobreescritura
        if (nombreArchivo.equals("obra1")) {
            MensajeDeAviso02 mensajeAvisoSobreescritura = new MensajeDeAviso02(null, true);
            mensajeAvisoSobreescritura.setVisible(true);
        }

        //Simulaci贸n de seleccion de impresora no disponible
        if (seleccion.equals("Canon PIXMA TS3451")) {
            MensajeDeAviso01 mensajeErrorImpresora = new MensajeDeAviso01(null, true);
            mensajeErrorImpresora.setVisible(true);
        } else if (seleccion.equals("EPSON SX525WD")) {
            JOptionPane.showMessageDialog(this, "El archivo se ha mandado a la lista de tareas de la impresora seleccionada.");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Archivo guardado como pdf");
            this.dispose();
        }


    }//GEN-LAST:event_jButtonImprimirActionPerformed

